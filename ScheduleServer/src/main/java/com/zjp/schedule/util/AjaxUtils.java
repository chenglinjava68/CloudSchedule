public class AjaxUtils {

    /**
     * 判断请求是否是ajax请求
     *
     * @param request 请求
     * @return
     */
    public static boolean isAjax(HttpServletRequest request) {
        return request.getHeader("X-Requested-With") != null
                && request.getHeader("X-Requested-With").equalsIgnoreCase("XMLHttpRequest");
    }
}
