public class ScheduleData {

    private String key;
    private String cron;
    private String appName;
    private String env;
    private String path;

    public ScheduleData() {
    }

    public ScheduleData(String key, String cron, String appName, String env, String path) {
        this.key = key;
        this.cron = cron;
        this.appName = appName;
        this.env = env;
        this.path = path;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getCron() {
        return cron;
    }

    public void setCron(String cron) {
        this.cron = cron;
    }

    public String getAppName() {
        return appName;
    }

    public void setAppName(String appName) {
        this.appName = appName;
    }

    public String getEnv() {
        return env;
    }

    public void setEnv(String env) {
        this.env = env;
    }
}
